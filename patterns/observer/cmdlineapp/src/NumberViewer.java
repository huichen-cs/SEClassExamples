public class NumberViewer {
    public static void displaySum(double sum) {
        System.out.println("The sum is " + sum);
    }

    public static void displayAverage(double average) {
        System.out.println("The average is " + average);
    }

    public static void displayMin(double value) {
        System.out.println("The min value is " + value);
    }

    public static void displayMax(double value) {
        System.out.println("The max value is " + value);
    }
}
